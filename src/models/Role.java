public class Role {
    private String id,
                   name,
                   descr;
    
    private char estatus;
    
    public Role(String id, String name, String descr, char estatus){
       this.id=id;
       this.name=name;
       this.descr=descr;
       this.estatus=estatus;
   }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }

    public char getEstatus() {
        return estatus;
    }

    public void setEstatus(char estatus) {
        this.estatus = estatus;
    }
    
    
}
